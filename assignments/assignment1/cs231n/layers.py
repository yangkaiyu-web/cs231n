def svm_loss(x, y):
    """
    Computes the loss and gradient using for multiclass SVM classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement loss and gradient for multiclass SVM classification.    #
    # This will be similar to the svm loss vectorized implementation in       #
    # cs231n/classifiers/linear_svm.py.                                       #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    N = x.shape[0]
    svm_matrix = x - np.reshape(x[np.arange(N),y],(N,-1)) + 1.0
    dx = (svm_matrix > 0) / N
    svm_sum = svm_matrix > 0
    svm_sum[np.arange(N),y] = 0
    svm_sum = np.reshape(np.sum(svm_sum,axis=1),(N,-1))
    mask = np.zeros(x.shape)
    mask[np.arange(N),y] = 1.0
    svm_sum = svm_sum * mask
    svm_matrix = svm_matrix * (svm_matrix > 0)
    svm_matrix[np.arange(N),y] = 0
    loss = 1/N * np.sum(svm_matrix)
    dx[np.arange(N),y] = 0
    dx -= 1/N * svm_sum

    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx


def softmax_loss(x, y):
    """
    Computes the loss and gradient for softmax classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement the loss and gradient for softmax classification. This  #    # will be similar to the softmax loss vectorized implementation in        #
    # cs231n/classifiers/softmax.py.                                          #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    N = x.shape[0]
    pre_X = x - np.reshape(np.max(x,axis=1),(x.shape[0],-1))

    # pre_EY:row vector
    pre_EY = np.reshape(np.exp(pre_X[np.arange(N),y]),(-1,N))
    pre_EX = np.exp(pre_X)
    # sum_EX:row vector
    sum_EX = np.reshape(np.sum(pre_EX,axis=1),(-1,N))
    loss = -np.sum(np.log(pre_EY / sum_EX)) / N

    mask = np.zeros(x.shape)
    mask[np.arange(N),y] = 1
    minuend_matrix = mask
    subtrahend_matrix = pre_EX / sum_EX.T
    dx = -1.0/N * (minuend_matrix - subtrahend_matrix)

    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx
